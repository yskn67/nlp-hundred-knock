########################################################################
# 02. 「パトカー」+「タクシー」=「パタトクカシーー」                   #
# 「パトカー」+「タクシー」の文字を先頭から交互に連結して              #
# 文字列「パタトクカシーー」を得よ.                                    #
########################################################################

#! /usr/bin/env python
# -*- coding: utf-8 -*-

_str1 = 'パトカー'
_str2 = 'タクシー'
merged_str = ''
for (bf, af) in zip(list(_str1), list(_str2)):
	merged_str = merged_str + bf + af
print(merged_str)
